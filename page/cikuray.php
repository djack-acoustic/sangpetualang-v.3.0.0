      	<h4 class="media-heading">Gunung Cikurai</h4></div>
        <p>Gunung yang memiliki ketinggian 2.818 mdpl ini merupakan salah satu gunung yang terletak di selatan kota Garut, Jawa Barat. Tepatnya berada di perbatasan kecamatan Bayongbong, Cikajang dan Dayeuh Manggung. Gunung Cikuray termasuk sebagai gunung yang tidak aktif dan identik dengan bentuknya yang menyerupai kerucut raksasa. Oleh karena itu, berdasarkan karakteristik bentuknya, di gunung ini sulit ditemukan mata air sepanjang perjalanan menuju puncak. Jalur pendakian gunung Cikuray sangat unik dan menantang. Trek yang terjal dan sulit menjadi tantangan tersendiri dalam pendakian gunung ini. Puncak gunung ini dapat ditempuh dengan waktu 7-8 jam pendakian.

Untuk mencapai puncak Cikuray, umumnya para pendaki mengawali perjalanannya melalui desa Dayeuh Manggung kecamatan Cilawu, Garut. Dibanding dengan jalur Bayongbong atau Cikajang, jalur ini relatif lebih mudah untuk didaki menuju puncak. Dari terminal bus Guntur, Garut, dapat dilanjutkan dengan menggunakan angkot bernomor 06 yang memiliki trayek Garut-Cilawu dengan tarif sekitar Rp. 3000,-. Lalu turun di Patrol dan kemudian lanjut menuju perkebunan teh Dayeuh Manggung. Dari Patrol, pendaki dapat menyewa jasa ojek dengan tarif sekitar Rp. 20.000. Bagi yang ingin berjalan kaki, perjalanan dapat ditempuh dalam waktu 2-3 jam.</p>

<div class="page-header"><h4>Pendakian Gunung Cikuray</h4></div>
<p>Perjalanan pemanasan ini akan berakhir di sebuah tempat yang biasa disebut Pemancar sebab di sana terdapat beberapa stasiun relay televisi seperti RCTI, TPI, TVRI, dan SCTV dan bisa dijadikan sebagai base camp sebelum melakukan pendakian. Selain itu, kebutuhan air bersih untuk pendakian bisa diambil di pos pemancar, setelah itu tidak akan ditemukan lagi sumber air bersih hingga menuju puncak. Kondisi jalan menuju pemancar sendiri merupakan jalan beraspal dan melewati pemukiman penduduk. Setelah itu akan melewati perkebunan teh dengan jalan berbatu dan cukup membosankan namun sangat indah pemandangannya.

 
pos Pemancar




 Setelah menyiapkan semuanya di camp Pemancar, pendakian dimulai dengan melalui jalan setapak perkebunan teh. Sehabis perkebunan teh, akan dijumpai jalur dengan bukit ilalang dan selanjutnya memasuki hutan yang semakin lebat ke dalamnya. Dari sinilah tantangan akan terjalnya gunung ini dapat dinikmati hingga menuju puncak bayangan. Waktu tempuh dari camp Pemancar menuju puncak bayangan sekitar 4-5 jam. Di sini pendaki dapat mendirikan tenda karena areanya yang cukup luas atau sekedar istirahat sebelum melanjutkan perjalanan ke puncak. Dari puncak bayangan, pendakian dilanjutkan agak ke kanan untuk menuju puncak Cikuray. Perjalanannya sendiri membutuhkan waktu sekitar 2- 3 jam pendakian. Jalur menuju puncak gunung ini sudah agak terbuka vegetasi hutannya dan berbatu sehingga dituntut untuk tetap hati-hati.

 
Jalur yang terjal dan menantang

Puncak bayangan
Hingga akhirnya sampai di puncak Cikuray, di sana terdapat sebuah shelter permanen yang bisa digunakan untuk tempat istirahat atau bermalam sekalipun. Namun sayang kelakuan 'Vandalisme' orang kita masih cukup tinggi. Terlihat di shelter ini banyak sekali coretan-coretan dari tangan-tangan yang tidak bertanggung jawab. Area puncak Cikuray cukup luas dan dapat memuat hingga sekitar 10 tenda. Namun angin di area ini cukup kencang, oleh karena itu untuk dapat dijadikan pertimbangan jika ingin mendirikan tenda di sini.
Terhampar pemandangan yang indah sekali dari puncak, Gunung Papandayan terlihat jelas dari area ini serta lautan awan yang sangat indah. Selain itu kita juga dapat menikmati pemandangan kota Garut dari ketinggian di area puncak Cikuray. Untuk mendaki gunung Cikuray disarankan pada musim pendakian, khususnya musim kemarau jika ingin menikmati sajian indah di puncak gunung.
Tak lupa penulis mengingatkan untuk selalu bersahabat dengan alam dengan tidak merusaknya.</p>



	
