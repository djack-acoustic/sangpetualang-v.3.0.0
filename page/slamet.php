      	<h4 class="media-heading">Gunung Slamet</h4></div>
        <p>Gunung Slamet (3.428 meter dpl.) adalah sebuah gunung berapi yang terdapat di Pulau Jawa. Gunung ini berada di perbatasan Kabupaten Brebes, Banyumas, Purbalingga, Kabupaten Tegal, dan Kabupaten Pemalang, Provinsi Jawa Tengah, dan merupakan yang tertinggi di Jawa Tengah serta kedua tertinggi di Pulau Jawa. Kawah IV merupakan kawah terakhir yang masih aktif sampai sekarang, dan terakhir aktif hingga pada level siaga medio-2009.</p>

<p>Gunung Slamet cukup populer sebagai sasaran pendakian meskipun medannya dikenal sulit. Di kaki gunung ini terletak kawasan wisata Baturraden yang menjadi andalan Kabupaten Banyumas karena hanya berjarak sekitar 15 km dari Purwokerto.</p>

<p>Sebagaimana gunung api lainnya di Pulau Jawa, Gunung Slamet terbentuk akibat subduksi Lempeng Indo-Australia pada Lempeng Eurasia di selatan Pulau Jawa. Retakan pada lempeng membuka jalur lava ke permukaan. Catatan letusan diketahui sejak abad ke-19. Gunung ini aktif dan sering mengalami erupsi skala kecil. Aktivitas terakhir adalah pada bulan Mei 2009 dan sampai Juni masih terus mengeluarkan lava pijar. Sebelumnya ia tercatat meletus pada tahun 1999.</p>

<p>Maret 2014 Gunung Slamet menunjukkan aktifitas dan statusnya menjadi Waspada. Berdasarkan data PVMBG, aktivitas vukanik Gunung Slamet masih fluktuatif. Setelah sempat terjadi gempa letusan hingga 171 kali pada Jumat 14 Maret 2014 dari pukul 00.00-12.00 WIB, pada durasi waktu yang sama, tercatat sebanyak 57 kali gempa letusan. Tercatat pula 51 kali embusan. Pemantauan visual, embusan asap putih tebal masih keluar dari kawah gunung ke arah timur hingga setinggi 1 km.<br />Gunung ini mempunyai kawasan hutan Dipterokarp Bukit, hutan Dipterokarp Atas, hutan Montana, dan Hutan Ericaceous atau hutan gunung.</p>

<div class="page-header"><h4>Sejarah Gunung Slamet</h4></div>
<p>Sejarawan Belanda, J. Noorduyn berteori bahwa nama "Slamet" adalah relatif baru, yaitu setelah masuknya Islam ke Jawa (kata itu merupakan pinjaman dari bahasa Arab). Ia mengemukakan pendapat bahwa yang disebut sebagai Gunung Agung dalam naskah berbahasa Sunda mengenai petualangan Bujangga Manik adalah Gunung Slamet, berdasarkan pemaparan lokasi yang disebutkan</p>  

<div class="page-header"><h4>Jalur Pendakian</h4></div>
<p>Jalur pendakian standar adalah dari Blambangan, Desa Kutabawa, Kecamatan Karangreja, Purbalingga. Jalur populer lain adalah dari Baturraden dan dari Desa Gambuhan, Desa Jurangmangu dan Desa Gunungsari di Kabupaten Pemalang. Selain itu adapula jalur yang baru saja diresmikan tahun 2013 lalu, yaitu jalur Dhipajaya yang terletak di Kabupaten Pemalang.</p>

<p>Pendakian Gunung Slamet dikenal cukup sulit karena hampir di sepanjang rute pendakian tidak ditemukan air. Pendaki disarankan untuk membawa persediaan air yang cukup dari bawah. Faktor penyulit lain adalah kabut. Kabut di Gunung Slamet sangat mudah berubah-ubah dan pekat.</p>

<p>Jalur pendakian lainnya adalah melalui obyek wisata pemandian air panas Guci, Kabupaten Tegal. Meskipun terjal, rute ini menyajikan pemandangan yang paling baik. Kawasan Guci dapat ditempuh dari Slawi menuju daerah Tuwel melewati Lebaksiu.</p>

