      	<h4 class="media-heading">Gunung sindoro</h4></div>
        <p>
        Gunung Sindara, biasa disebut Sindoro, atau juga Sundoro (altitudo 3.150 meter di atas permukaan laut) merupakan sebuah gunung volkano aktif yang terletak di Jawa Tengah, Indonesia, dengan Temanggung sebagai kota terdekat. Gunung Sindara terletak berdampingan dengan Gunung Sumbing.

Kawah yang disertai jurang dapat ditemukan di sisi barat laut ke selatan gunung, dan yang terbesar disebut Kembang. Sebuah kubah lava kecil menempati puncak gunung berapi. Sejarah letusan Gunung Sindara yang telah terjadi sebagian besar berjenis ringan sampai sedang (letusan freatik).

Hutan di kawasan Gunung Sundoro mempunyai bertipe hutan Dipterokarp Bukit, hutan Dipterokarp Atas, hutan Montane, dan Hutan Ericaceous atau hutan gunun
</p></br>
<h4>Jalur pendakian via Kledung</h4>
<p>sebelum pendakian terlebih dahulu kita registrasi di basecamp kledung. dikarenakan tempatnya lumayan besar dan ditunjang fasilitas MCK, para pendaki memanfaatkan untuk istirahat terlebih dahulu sebelum memulai pendakian. dari basecamp kledung ke pos satu kita akan melalui jalan berbatu rapi dengan lahan pertanian disampingnya sejauh sekitar 2km dan hutan pinus setelahnya menjelang Pos 1 Sibajing, dengan ketinggian 1.900 mdpl. ingat kita harus isi penuh tempat air kita dikarenakan selama pendakian tidak dijumpai mata air selain di puncak 
 
Dari pos 1 kita berbelok ke kanan, kita akan mendaki dan menuruni 2 buah punggungan gunung.
Jalur bergeser kepunggungan yang lain melintasi beberapa jembatan kayu.pohon pinus dan lamtoro selama perjalanan akan membawa suasana sejuk.
