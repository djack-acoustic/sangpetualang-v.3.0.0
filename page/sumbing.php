Kawah
Sebagian besar pendaki akan mengambil jalur lurus ke arah Puncak Buntu, namun sebenarnya Puncak Kawah memiliki posisi yang lebih tinggi dibanding Puncak Buntu meskipun bukan pucuk tertinggi.
